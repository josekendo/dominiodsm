public int New_ (ReporteEN reporte)
{
        try
        {
                SessionInitializeTransaction ();
                if (reporte.Publicacion != null) {
                        // Argumento OID y no colecci√≥n.
                        reporte.Publicacion = (DominiolifetagGenNHibernate.EN.Dominiolifetag.PublicacionEN)session.Load (typeof(DominiolifetagGenNHibernate.EN.Dominiolifetag.PublicacionEN), reporte.Publicacion.ID);

                        reporte.Publicacion.Reporte
                                = reporte;
                }

                session.Save (reporte);
                SessionCommit ();
        }

        catch (Exception ex) {
                SessionRollBack ();
                if (ex is DominiolifetagGenNHibernate.Exceptions.ModelException)
                        throw ex;
                throw new DominiolifetagGenNHibernate.Exceptions.DataLayerException ("Error in ReporteCAD.", ex);
        }


        finally
        {
                SessionClose ();
        }

        return reporte.ID;
}

